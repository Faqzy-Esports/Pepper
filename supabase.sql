-- Supabase SQL schema for Pepper resource pack uploads
-- Create a public storage bucket for ZIP uploads.
-- If this fails, create the bucket directly in the Supabase Dashboard Storage panel.
insert into storage.buckets (id, name, public)
values ('packs', 'packs', true)
on conflict (id) do update set public = excluded.public, name = excluded.name;

-- Supabase storage bucket is created as public above. Do not modify storage system tables from this app schema.

-- Storage policies for the packs bucket (allows uploads and downloads)
DROP POLICY IF EXISTS "Allow public read packs bucket" ON storage.objects;
CREATE POLICY "Allow public read packs bucket"
  ON storage.objects FOR SELECT
  USING (bucket_id = 'packs');

DROP POLICY IF EXISTS "Allow public upload to packs bucket" ON storage.objects;
CREATE POLICY "Allow public upload to packs bucket"
  ON storage.objects FOR INSERT
  WITH CHECK (bucket_id = 'packs');

DROP POLICY IF EXISTS "Allow public update packs bucket" ON storage.objects;
CREATE POLICY "Allow public update packs bucket"
  ON storage.objects FOR UPDATE
  USING (bucket_id = 'packs')
  WITH CHECK (bucket_id = 'packs');

DROP POLICY IF EXISTS "Allow public delete packs bucket" ON storage.objects;
CREATE POLICY "Allow public delete packs bucket"
  ON storage.objects FOR DELETE
  USING (bucket_id = 'packs');

-- Create a packs metadata table
CREATE TABLE IF NOT EXISTS packs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    author TEXT NOT NULL,
    category TEXT NOT NULL,
    description TEXT NOT NULL,
    "fullDescription" TEXT,
    version TEXT,
    downloads INTEGER DEFAULT 0,
    likes INTEGER DEFAULT 0,
    cover TEXT,
    gallery JSONB DEFAULT '[]'::jsonb,
    versions JSONB DEFAULT '[]'::jsonb,
    "uploadedBy" TEXT,
    featured BOOLEAN DEFAULT FALSE,
    "uploadDate" TIMESTAMPTZ DEFAULT NOW(),
    tags TEXT[] DEFAULT '{}',
    compatibility TEXT[] DEFAULT '{}',
    rating NUMERIC(3,2) DEFAULT 0,
    downloads_all_time INTEGER DEFAULT 0,
    "downloadUrl" TEXT,
    "filePath" TEXT
);

-- Enable row-level security for pack metadata and allow anonymous clients to access packs.
ALTER TABLE packs ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Allow anon select on packs" ON packs;
CREATE POLICY "Allow anon select on packs"
    ON packs FOR SELECT
    USING (true);

DROP POLICY IF EXISTS "Allow anon insert on packs" ON packs;
CREATE POLICY "Allow anon insert on packs"
    ON packs FOR INSERT
    WITH CHECK (true);

DROP POLICY IF EXISTS "Allow anon update on packs" ON packs;
CREATE POLICY "Allow anon update on packs"
    ON packs FOR UPDATE
    USING (true)
    WITH CHECK (true);

DROP POLICY IF EXISTS "Allow anon delete on packs" ON packs;
CREATE POLICY "Allow anon delete on packs"
    ON packs FOR DELETE
    USING (true);

CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email TEXT UNIQUE NOT NULL,
    username TEXT UNIQUE NOT NULL,
    password_hash TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    liked_packs JSONB DEFAULT '[]'::jsonb,
    uploaded_packs JSONB DEFAULT '[]'::jsonb,
    display_name TEXT,
    bio TEXT,
    avatar_url TEXT,
    website TEXT
);

