-- Supabase SQL schema for Pepper resource pack uploads
-- Create a public storage bucket for ZIP uploads
SELECT storage.create_bucket('packs', true);

-- Ensure the bucket is public so uploaded files are accessible via public URLs
SELECT storage.update_bucket('packs', true);

-- Create a packs metadata table
CREATE TABLE IF NOT EXISTS packs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    author TEXT NOT NULL,
    category TEXT NOT NULL,
    description TEXT NOT NULL,
    fullDescription TEXT,
    version TEXT,
    downloads INTEGER DEFAULT 0,
    likes INTEGER DEFAULT 0,
    cover TEXT,
    gallery JSONB DEFAULT '[]'::jsonb,
    versions JSONB DEFAULT '[]'::jsonb,
    uploadedBy TEXT,
    featured BOOLEAN DEFAULT FALSE,
    uploadDate TIMESTAMPTZ DEFAULT NOW(),
    tags TEXT[] DEFAULT '{}',
    compatibility TEXT[] DEFAULT '{}',
    rating NUMERIC(3,2) DEFAULT 0,
    downloads_all_time INTEGER DEFAULT 0,
    downloadUrl TEXT,
    filePath TEXT
);
