const fs = require('fs');
const path = require('path');

const url = process.env.RENDER_SUPABASE_URL || process.env.SUPABASE_URL || null;
const anonKey = process.env.RENDER_SUPABASE_ANON_KEY || process.env.SUPABASE_ANON_KEY || null;
const destPath = path.join(__dirname, 'js', 'supabase-config.js');

const fileContents = `// Generated by build-supabase-config.js
const url = ${JSON.stringify(url)};
const anonKey = ${JSON.stringify(anonKey)};

window.PEPPER_SUPABASE = {
  url,
  anonKey
};

if (!url || !anonKey) {
  console.warn('Supabase URL and anon key were not provided during build. Set them in Render environment variables.');
}
`;

fs.writeFileSync(destPath, fileContents, 'utf8');
console.log(`Generated ${destPath}`);
